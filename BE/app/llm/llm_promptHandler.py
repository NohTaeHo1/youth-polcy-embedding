
def rag_prompt(context: str, query: str) -> str:
    """RAG 프롬프트 생성"""
    return f"""다음은 청년 정책 데이터입니다:

    {context}

    사용자 질문: {query}

    위 정책 정보를 바탕으로 사용자의 질문에 답변해 주세요. 답변은 다음과 같은 형식을 따라야 합니다:
    1. **정책명**: 정책의 제목을 명확히 명시하세요.
    2. **주요 내용**: 정책의 주요 지원 내용과 목적을 간결히 설명하세요.
    3. **지원 대상**: 지원 대상 나이와 기타 조건을 포함하세요.
    4. **신청 방법**: 신청 방법과 절차를 구체적으로 설명하세요.
    5. **신청 기간**: 신청 가능 기간을 명시하세요.
    6. **주관기관 및 연락처**: 주관기관과 연락처 정보를 포함하세요.
    7. **추가 정보**: 참고 URL 또는 기타 유의사항이 있으면 포함하세요.

    답변은 간결하고 명확해야 하며, 사용자가 정책을 쉽게 이해하고 신청할 수 있도록 도와야 합니다. 불필요한 정보는 제외하고, 위 형식에 맞춰 번호를 붙여 구조화된 답변을 제공하세요.
    """

